    public void destroy() {
        removeAll();
        
        // Destroy the StatEditors
        Iterator i = statEditorVector.iterator();
        while ( i.hasNext() ) {
            TargetStatEditor2 tse = (TargetStatEditor2) i.next();
            tse.destroy();
        }
    }
    
