    private void setPortraitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setPortraitActionPerformed
        // Add your handling code here:
        if ( target == null ) return;
        MyFileChooser chooser = MyFileChooser.chooser;
        
        MyFileFilter mff = new MyFileFilter(new String[] {"gif", "jpg"}, "Select Character Portrait");
        chooser.setFileFilter(mff);
        
        chooser.setDialogTitle( "Open" );
        
        int returnVal = chooser.showOpenDialog(null);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            try {
                File file = chooser.getSelectedFile();
                if ( file != null ) target.setImageFile(file);
            }
            catch (Exception exc) {
                JOptionPane.showMessageDialog(null,
                "An Error Occurred while opening:\n" +
                exc.toString(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_setPortraitActionPerformed
    
    
