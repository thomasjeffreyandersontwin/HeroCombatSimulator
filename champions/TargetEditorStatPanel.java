    public void PADValueChanged(PADValueEvent evt) {
        if ( target != null) {
            if ( evt.getKey().equals("Target.NAME") ) {
                target.setName( (String)evt.getValue() );
            }
            else {
                target.add( evt.getKey(), evt.getValue(), true);
            }
        }
    }
    
