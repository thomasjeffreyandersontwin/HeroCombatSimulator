    /** Creates new form RealTreePanel */
    public ScrollableTreePanel(TreeModel model, TreeCellRenderer renderer) {
        initComponents();
        jTree1.setModel( model);
        
        ContextMenu.addContextMenu(jTree1);
        
        if ( renderer != null ) {
            jTree1.setCellRenderer(renderer);
        }
        
        expandAll();
    }
    
    public static void showTreePanel(TreeModel model) {
        showTreePanel(model, null);
    }
    
    public static void showTreePanel(TreeModel model, TreeCellRenderer renderer) {
        JFrame frame = new JFrame();
        frame.getContentPane().add( new ScrollableTreePanel(model, renderer) );
        frame.pack();
        
        frame.show();
    }
    
    public void expandAll() {
        expandAll( new TreePath( jTree1.getModel().getRoot() ) );
    }
    
    public void expandAll(TreePath path) {
        
        jTree1.expandPath(path);
        
        Object node = path.getLastPathComponent();
        int count = jTree1.getModel().getChildCount( node );
        for(int index = 0; index < count; index++) {
            TreePath newPath = path.pathByAddingChild( jTree1.getModel().getChild(node, index));
            expandAll( newPath);
        }
    }
    
