    public boolean invokeMenu(JPopupMenu popup, Component inComponent, Point inPoint) {
        TreePath tp = jTree1.getSelectionPath();
        if ( tp != null && Battle.debugLevel >= 1) {
            if ( tp.getLastPathComponent() instanceof AttackTreeNode ) {
                final AttackTreeNode atn = (AttackTreeNode)tp.getLastPathComponent();
                popup.add( new AbstractAction("Debug Node's BattleEvent") {
                    public void actionPerformed(ActionEvent e ) {
                        atn.getBattleEvent().debugDetailList(atn.getName() + "'s BattleEvent");
                    }
                });
                return true;
            }
        }
        return false;
    }    
    
    
